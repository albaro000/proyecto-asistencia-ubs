    public void cargarArchivoEstudiante()
    {
       File archi=new File(dirArchivo); 
       
       if(!archi.exists())
           try{
               archi.createNewFile();
           } catch(IOException ex){
               Logger.getLogger(FORM_ASISTENCIA.class.getName()).log(Level.SEVERE,null,ex);
           }
       else{
       try{
           FileReader frest=new FileReader(dirArchivo);
           BufferedReader br=new BufferedReader(frest);
           String linea=null;
           //llevamos los daatos del archivo al vector
           linea=br.readLine();
           while(linea!=null)
           {
               //separar la cadena linea separado por el token
               StringTokenizer st=new StringTokenizer(linea,",");
               est=new ESTUDIANTE();
               int cod=Integer.parseInt(st.nextToken());
               est.setRU(cod);
               est.setNombre(st.nextToken());
               est.setApellido(st.nextToken());
               est.setGenero(st.nextToken().charAt(0));
               ve.agregarEstudiante(est);
               linea=br.readLine();
           }
           br.close();
       }catch(Exception ex)
       {
           mensaje("ERROR AL CARGAR EL ARCHIVO!!!"+ex.getMessage());
       }
    }}         //ALBARO CALLIZAYA MOLLO
    public void listarEstudiantes()
    {
        Object fila[]=new Object[dtm.getColumnCount()];
        
        for(int i=0; i<ve.cantidadEstudiante();i++)
        {
            est=ve.obtenerEstudiante(i);
            fila[0]=est.getRU();
            fila[1]=est.getNombre();
            fila[2]=est.getApellido();
            fila[3]=est.getGenero();
            dtm.addRow(fila);
        }
        tablaEstudiantes.setModel(dtm);
    }
