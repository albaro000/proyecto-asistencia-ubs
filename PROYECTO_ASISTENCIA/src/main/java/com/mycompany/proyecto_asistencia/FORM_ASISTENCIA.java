        void registrarEstudiante(File dirArchivo)
        {
            try{
                if(leerRU()==99) mensaje("DEBE INGRESAR UN NUMERO ENTERO");
                else if(leerNombre()==null)mensaje("INGRESE EL NOMBRE");
                else if(leerApellido()==null)mensaje("INGRESE EL APELLIDO");
                else
                {
                    char genero;
                    if(brMasculino.isSelected())
                        genero='M';
                    else genero='F';
                    
                    est=new ESTUDIANTE(leerRU(), leerNombre(), leerApellido(),genero);
                    if((ve.buscarRUEstudiante(est.getRU()))!=-1)
                    {
                        mensaje("ESTE CODIGO YA EXISTE");
                    }
                    else{
                        ve.agregarEstudiante(est);
                    }
                    grabarEstudiante();
                    limpiar();
                    listarEstudiantes();
                }
            }catch(Exception ex)
            {
                mensaje("ERROR AL ABRIR EL ARCHIVO");
            }
        }
        void grabarEstudiante() throws IOException// implementado sin saber
        {
            FileWriter fw;
            PrintWriter pw;
            try{            //ALBARO CALLIZAYA MOLLO
                fw=new FileWriter(dirArchivo);
                pw=new PrintWriter(fw);
                for(int i=0;i<ve.cantidadEstudiante();i++)
                {
                    est=ve.obtenerEstudiante(i);
                    pw.println(String.valueOf(est.getRU())+", "+est.getNombre()+
                    ", "+est.getApellido()+", "+
                            est.getGenero());
                }
                pw.close();
            }
            catch(Exception ex)
            {
                mensaje("ERROR AL GRABAR EL ARCHIVO");
            }
        }
        void limpiar()
        {
            limpiarTabla();
            txtRU.setText(null);
            txtNombre.setText(null);
            txtApellido.setText(null);
            brMasculino.setSelected(true);
            limpiarTabla();
        }
        void limpiarTabla()
        {
            for(int i=0; i<tablaEstudiantes.getRowCount();i++){
                dtm.removeRow(i);
                i-=1;
            }
        }       //ALBARO CALLIZAYA MOLLO
        char leerGenero()
        {
            if(brMasculino.isSelected())
                return 'M';
            else return 'F';
        }
        void modificarEstudiante(File dirArch)
        {
            try{
                if(leerRU()==99)mensaje("INGRESAR RU ENTERO");
                else if(leerNombre()==null)mensaje("INGRESE EL NOMBRE");
                else if(leerApellido()==null)mensaje("INGRESAR APELLIDOS");
                else 
                {
                    int RU=ve.buscarRUEstudiante(leerRU());
                    est=new ESTUDIANTE(leerRU(),leerNombre(),leerApellido(),leerGenero());
                    if(RU==-1)
                        ve.agregarEstudiante(est);
                    else{
                        ve.modificarEstudiante(RU, est);
                    }
                    grabarEstudiante();
                    limpiar();
                    listarEstudiantes();
                } 
            }catch(Exception ex){
                mensaje("ERROR AL ABRIR EL ARCHIVO PARA MODIFICAR");
            }
        }
        void eliminarEstudiante()
        {        //ALBARO CALLIZAYA MOLLO
            try
            {
                if(leerRU()==-99)mensaje("INGRESAR UN RU ENTERO");
                else
                {
                    int RU=ve.buscarRUEstudiante(leerRU());
                    if(RU==-1)
                    {
                        mensaje("RU NO EXISTENTE");
                    }else{
                        int s=JOptionPane.showConfirmDialog(null, "ESTA SEGURO DE "
                        +"ELIMINAR ESTE ESTUDIANTE?","SI/NO",0);
                        if(s==0)
                        {
                            ve.eliminarEstudiante(RU);
                            grabarEstudiante();
                            limpiar();
                            listarEstudiantes();
                        }      //ALBARO CALLIZAYA MOLLO
                    }
                    
                }
            }catch(Exception ex){
                mensaje ("NO SE PUDO ELIMINAR A ESTE ESTUDIANTE");
            }
        }
        
